#!/usr/bin/env node
/**
 * Extract the "PK" glyph outlines from JetBrains Mono ExtraBold and emit
 * an SVG path string usable in icons, components, and the OG template.
 *
 * Spec mirrors logo.html T02:
 *   font:    JetBrains Mono ExtraBold (weight 800)
 *   text:    "PK"
 *   size:    60 units
 *   tracking: -0.06em (CSS letter-spacing)
 *   viewBox: 100×100, glyph centered horizontally, baseline at y=62
 *
 * Re-run when the type spec changes.
 *   node scripts/extract-pk-glyph.mjs
 */
import { writeFileSync, existsSync, mkdirSync, readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { tmpdir } from 'node:os';
import opentype from 'opentype.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const FONT_URL =
  'https://github.com/JetBrains/JetBrainsMono/raw/master/fonts/ttf/JetBrainsMono-ExtraBold.ttf';
const FONT_CACHE = resolve(tmpdir(), 'protokol-jbm-extrabold.ttf');

const TEXT = 'PK';
const FONT_SIZE = 60;
const TRACKING_EM = -0.06;
const VIEWBOX = 100;
const BASELINE_Y = 62;

async function ensureFont() {
  if (existsSync(FONT_CACHE)) return readFileSync(FONT_CACHE);
  console.log('[brand] downloading', FONT_URL);
  const res = await fetch(FONT_URL);
  if (!res.ok) throw new Error(`font fetch failed: ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  writeFileSync(FONT_CACHE, buf);
  return buf;
}

function buildPath(font) {
  const tracking = TRACKING_EM * FONT_SIZE; // px gap added after each glyph
  const glyphs = font.stringToGlyphs(TEXT);
  const advances = glyphs.map(
    (g) => (g.advanceWidth / font.unitsPerEm) * FONT_SIZE,
  );
  const totalWidth =
    advances.reduce((a, b) => a + b, 0) + tracking * (glyphs.length - 1);
  const startX = (VIEWBOX - totalWidth) / 2;

  let cursor = startX;
  const parts = [];
  for (let i = 0; i < glyphs.length; i++) {
    const p = glyphs[i].getPath(cursor, BASELINE_Y, FONT_SIZE);
    parts.push(p.toPathData(3));
    cursor += advances[i] + tracking;
  }
  return parts.join(' ');
}

const fontBuf = await ensureFont();
const font = opentype.parse(
  fontBuf.buffer.slice(fontBuf.byteOffset, fontBuf.byteOffset + fontBuf.byteLength),
);

const d = buildPath(font);

const outDir = resolve(ROOT, 'src/brand');
mkdirSync(outDir, { recursive: true });

const moduleSrc = `// Auto-generated by scripts/extract-pk-glyph.mjs — do not edit by hand.
// Source: JetBrains Mono ExtraBold (weight 800), text "PK",
// size 60, tracking -0.06em, baseline y=62, 100×100 viewBox.

export const PK_VIEWBOX = '0 0 ${VIEWBOX} ${VIEWBOX}';
export const PK_PATH = ${JSON.stringify(d)};
`;

writeFileSync(resolve(outDir, 'pk-glyph.js'), moduleSrc);
console.log('[brand] wrote src/brand/pk-glyph.js');
console.log('[brand] path length:', d.length, 'chars');
